#!/usr/bin/env node
// Regenerates packages/shared/biome-plugins/no-constants-via-i18n.grit
// from public/locales/.constants/*.json.
//
// These keys represent brand names, product names, and other runtime
// constants that must be accessed via getBrandName()/getConstName()/
// getCultureLabel() helpers — NOT through i18next's t() function.
// See ConstantsViaI18nTest in common/tests/test/locales.test.js for
// the same invariant enforced at test time.

const fs = require("fs");
const path = require("path");

const CONSTANTS_DIR = path.join(
  __dirname,
  "..",
  "public",
  "locales",
  ".constants",
);
const OUT_FILE = path.join(
  __dirname,
  "..",
  "packages",
  "shared",
  "biome-plugins",
  "no-constants-via-i18n.grit",
);

function loadKeys(fileName) {
  const filePath = path.join(CONSTANTS_DIR, fileName);
  const raw = JSON.parse(fs.readFileSync(filePath, "utf8"));
  // Strip locale suffixes (e.g. "ProviderApple-si" → "ProviderApple").
  // Locale-suffixed keys are lookup variants handled by helpers, not i18n keys.
  return new Set(Object.keys(raw).map((k) => k.split("-")[0]));
}

const brandKeys = loadKeys("brands.json");
const constKeys = loadKeys("consts.json");
const cultureCodes = Object.keys(
  JSON.parse(
    fs.readFileSync(path.join(CONSTANTS_DIR, "cultures.json"), "utf8"),
  ),
);

const baseKeys = new Set([...brandKeys, ...constKeys]);

// For each base key, forbid both bare (`t("Key")`) and namespaced
// (`t("Common:Key")`) forms. i18next allows either depending on default
// namespace configuration, so both need to be caught.
const forbidden = [];
for (const k of [...baseKeys].sort()) {
  forbidden.push(k);
  forbidden.push(`Common:${k}`);
}
// Legacy culture keys — no locale file currently uses Culture_*, but guard
// against regressions.
for (const code of cultureCodes.sort()) {
  forbidden.push(`Culture_${code}`);
  forbidden.push(`Common:Culture_${code}`);
}

const alternatives = forbidden.map((k) => `        \`"${k}"\``).join(",\n");

const grit = `// AUTO-GENERATED by scripts/generate-biome-i18n-constants-plugin.js
// Do not edit manually. Regenerate after changing public/locales/.constants/*.json:
//   node scripts/generate-biome-i18n-constants-plugin.js
//
// Forbids passing brand/constant/culture keys through i18next's \`t()\` function.
// These constants live in public/locales/.constants/ and must be accessed via
// getBrandName(), getConstName(), or getCultureLabel() helpers. Using \`t()\`
// introduces a race against i18n init and splits the single source of truth.

\`t($key, $...)\` where {
    $key <: or {
${alternatives}
    },
    register_diagnostic(
        span = $key,
        message = "Brand/constant/culture keys must be accessed via getBrandName(), getConstName(), or getCultureLabel() — not through i18next t().",
        severity = "error"
    )
}
`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, grit);
console.log(`Wrote ${OUT_FILE}`);
console.log(`Forbidden patterns: ${forbidden.length}`);
